    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        // generate new random number
        int num1,num2,answer;

        Random rand = new Random();

        num1 = rand.nextInt(40)+1;
        num2 = rand.nextInt(20)+1;
        
        
        if(num1%2==1) num1++;
        
        for(int i=num2-1 ; i>=1 ; i--)
        {
            if(num1%i==0)
            {
                num2=i;
                break;
            }
        }
        
       
        

        n1.setText(Integer.toString(num1));
        n2.setText(Integer.toString(num2));

        judge.setText("Judgement");
        ansText.setText("");

    }//GEN-LAST:event_jLabel5MouseClicked

    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        // calculation

        String number1,number2,answer;
        number1 = n1.getText();
        number2 = n2.getText();
        answer = ansText.getText();

        int n1 = Integer.parseInt(number1);
        int n2 = Integer.parseInt(number2);
        int ansT = Integer.parseInt(answer);
            
        int ans = n1 / n2;

        if(ans == ansT)
        {
            judge.setText("Excellent !!!!");
             ProgressNumber.correct_no_div++;
        }
        else
        {
            judge.setText("Wrong !! The correct answer is "+ans);
             ProgressNumber.wrong_no_div++;
        }

    }//GEN-LAST:event_jLabel6MouseClicked

